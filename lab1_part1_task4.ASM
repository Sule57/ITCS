	CLO
	JMP START
	DB 0
	DB 0
START:
	MOV AL, [03]
	CMP AL, 6
	JS MORE
	JMP DISPLAY
TRUE:
	MOV BL, [04]
	ADD BL, 3
	MOV [04], BL
NEXT:
	ADD AL, 1
	MOV [03],AL
	JMP START
MORE:
	CMP AL, 3
	JZ TRUE
	JMP NEXT
DISPLAY:
	MOV AL,[03]
	ADD AL, 30
	MOV BL,[04]
	ADD BL, 30
	MOV [C0],AL
	MOV [D1],BL
	END
	